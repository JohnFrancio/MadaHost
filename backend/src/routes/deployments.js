async function deployProject(deploymentId, project) {
  let primaryFramework = null;
  let buildLog = "";
  const deploymentDir = path.join(__dirname, "../../temp", deploymentId);

  try {
    console.log(
      `üöÄ D√©marrage d√©ploiement ${deploymentId} pour ${project.name}`
    );

    const subdomain = project.name.toLowerCase().replace(/[^a-z0-9]/g, "-");
    const outputDir = path.join("/var/www/deployed", subdomain);

    buildLog += `üöÄ [${new Date().toISOString()}] D√©marrage du d√©ploiement...\n`;
    buildLog += `üìÅ Dossier de sortie: ${outputDir}\n`;
    await updateDeploymentLog(deploymentId, buildLog);

    await fs.mkdir(deploymentDir, { recursive: true });
    await fs.mkdir(outputDir, { recursive: true });

    const { data: user } = await supabase
      .from("users")
      .select("access_token")
      .eq("id", project.user_id)
      .single();

    if (!user?.access_token) {
      throw new Error("Token GitHub manquant pour l'utilisateur");
    }

    // ==================== CLONAGE ====================
    buildLog += `üì• [${new Date().toISOString()}] Clonage de ${
      project.github_repo
    }...\n`;
    await updateDeploymentLog(deploymentId, buildLog);

    const cloneCommand = `git clone --depth 1 -b ${
      project.branch || "main"
    } https://${user.access_token}@github.com/${
      project.github_repo
    }.git ${deploymentDir}`;

    await supabase
      .from("deployments")
      .update({ status: "cloning", build_log: buildLog })
      .eq("id", deploymentId);

    await execCommand(cloneCommand, {}, 300000, null, deploymentDir);
    buildLog += `‚úÖ Repository clon√© avec succ√®s\n`;

    try {
      const commitHash = await execCommand(
        "git rev-parse HEAD",
        {},
        30000,
        deploymentDir,
        deploymentDir
      );

      await supabase
        .from("deployments")
        .update({ commit_hash: commitHash.trim() })
        .eq("id", deploymentId);
      buildLog += `üìã Commit: ${commitHash.trim().substring(0, 8)}\n`;
    } catch (error) {
      buildLog += `‚ö†Ô∏è Impossible de r√©cup√©rer le commit hash\n`;
    }

    // ==================== D√âTECTION FRAMEWORKS ====================
    buildLog += `üîç [${new Date().toISOString()}] D√©tection des frameworks...\n`;
    await updateDeploymentLog(deploymentId, buildLog);

    const frameworkHandler = new UniversalFrameworkHandler();
    const {
      frameworks: detectedFrameworks,
      configs: frameworkConfigs,
      log: detectionLog,
    } = await frameworkHandler.detectFrameworks(deploymentDir);
    buildLog += detectionLog;

    let finalBuildCommand = project.build_command;
    let finalOutputDir = project.output_dir || "dist";
    let finalInstallCommand = project.install_command || "npm install";

    if (frameworkConfigs.length > 0) {
      primaryFramework = frameworkConfigs[0];
      buildLog += `üéØ Framework principal d√©tect√©: ${
        primaryFramework.name
      } (${Math.round(primaryFramework.confidence * 100)}%)\n`;

      // Dans la d√©tection des frameworks - AJOUTEZ cette v√©rification
      if (
        primaryFramework?.name === "react" ||
        primaryFramework?.name === "vue"
      ) {
        // ‚úÖ V√©rifier si le projet utilise Vite explicitement
        const packageJsonPath = path.join(deploymentDir, "package.json");
        try {
          const packageJson = JSON.parse(
            await fs.readFile(packageJsonPath, "utf8")
          );
          const scripts = packageJson.scripts || {};
          const allDeps = {
            ...packageJson.dependencies,
            ...packageJson.devDependencies,
          };

          const usesVite = scripts.build?.includes("vite") || allDeps.vite;

          if (usesVite) {
            buildLog += `‚ö° Projet configur√© avec Vite\n`;

            // ‚úÖ S'assurer que les plugins Vite sont pr√©sents
            if (
              primaryFramework.name === "react" &&
              !allDeps["@vitejs/plugin-react"]
            ) {
              buildLog += `‚ûï Ajout de @vitejs/plugin-react...\n`;
              await execCommand(
                "npm install @vitejs/plugin-react@^4.0.0 --save-dev --no-audit --no-fund",
                {},
                120000,
                deploymentDir,
                deploymentDir
              );
            }

            if (
              primaryFramework.name === "vue" &&
              !allDeps["@vitejs/plugin-vue"]
            ) {
              buildLog += `‚ûï Ajout de @vitejs/plugin-vue...\n`;
              await execCommand(
                "npm install @vitejs/plugin-react@^4.0.0 --save-dev --no-audit --no-fund",
                {},
                120000,
                deploymentDir,
                deploymentDir
              );
            }
          }
        } catch (error) {
          buildLog += `‚ö†Ô∏è Impossible de v√©rifier la config Vite: ${error.message}\n`;
        }
      }

      // ‚úÖ CORRECTION: Utiliser les bonnes commandes selon le framework
      if (primaryFramework.name === "nextjs") {
        // Next.js utilise ses propres commandes
        finalBuildCommand = "npm run build";
        finalOutputDir = ".next"; // ou "out" si export statique
        finalInstallCommand = "npm install";

        buildLog += `üì¶ Next.js d√©tect√© - Configuration sp√©ciale\n`;

        // V√©rifier si export statique est configur√©
        const packageJsonPath = path.join(deploymentDir, "package.json");
        try {
          const packageJson = JSON.parse(
            await fs.readFile(packageJsonPath, "utf8")
          );
          if (packageJson.scripts?.export) {
            finalBuildCommand = "npm run build && npm run export";
            finalOutputDir = "out";
            buildLog += `üì§ Export statique Next.js configur√©\n`;
          }
        } catch (e) {
          buildLog += `‚ö†Ô∏è Impossible de v√©rifier l'export statique\n`;
        }
      } else {
        // Pour les autres frameworks, utiliser la config d√©tect√©e
        if (
          !project.build_command ||
          project.build_command === "npm run build"
        ) {
          finalBuildCommand = primaryFramework.config.buildCommand;
          buildLog += `üîß Commande de build automatique: ${finalBuildCommand}\n`;
        }

        if (!project.output_dir || project.output_dir === "dist") {
          finalOutputDir = primaryFramework.config.outputDir;
          buildLog += `üìÅ Dossier de sortie automatique: ${finalOutputDir}\n`;
        }

        if (
          !project.install_command ||
          project.install_command === "npm install"
        ) {
          finalInstallCommand = primaryFramework.config.installCommand;
        }
      }
    }

    // Setup des frameworks (mais pas pour Next.js !)
    // ==================== MODIFICATION DU PACKAGE.JSON ====================
    if (
      primaryFramework?.name === "react" ||
      primaryFramework?.name === "vue"
    ) {
      try {
        const packageJsonPath = path.join(deploymentDir, "package.json");
        const packageJson = JSON.parse(
          await fs.readFile(packageJsonPath, "utf8")
        );

        buildLog += `üìã [${new Date().toISOString()}] V√©rification des d√©pendances...\n`;

        const allDeps = {
          ...packageJson.dependencies,
          ...packageJson.devDependencies,
        };

        let modified = false;

        // ‚úÖ Ajouter Vite si manquant
        if (!allDeps.vite) {
          if (!packageJson.devDependencies) packageJson.devDependencies = {};
          packageJson.devDependencies.vite = "^5.0.0";
          modified = true;
          buildLog += `‚ûï Ajout de Vite v5.0.0\n`;
        }

        // ‚úÖ Ajouter le plugin React/Vue si manquant
        if (
          primaryFramework.name === "react" &&
          !allDeps["@vitejs/plugin-react"]
        ) {
          packageJson.devDependencies["@vitejs/plugin-react"] = "^4.2.0";
          modified = true;
          buildLog += `‚ûï Ajout de @vitejs/plugin-react v4.2.0\n`;
        } else if (
          primaryFramework.name === "vue" &&
          !allDeps["@vitejs/plugin-vue"]
        ) {
          packageJson.devDependencies["@vitejs/plugin-vue"] = "^5.0.0";
          modified = true;
          buildLog += `‚ûï Ajout de @vitejs/plugin-vue v5.0.0\n`;
        }

        if (modified) {
          await fs.writeFile(
            packageJsonPath,
            JSON.stringify(packageJson, null, 2)
          );
          buildLog += `üíæ package.json mis √† jour\n`;
        } else {
          buildLog += `‚úÖ Toutes les d√©pendances n√©cessaires sont pr√©sentes\n`;
        }

        await updateDeploymentLog(deploymentId, buildLog);
      } catch (error) {
        buildLog += `‚ö†Ô∏è Erreur modification package.json: ${error.message}\n`;
      }
    }

    // ==================== INSTALLATION DES D√âPENDANCES ====================
    const packageJsonPath = path.join(deploymentDir, "package.json");
    try {
      await fs.access(packageJsonPath);

      buildLog += `üì¶ [${new Date().toISOString()}] Installation des d√©pendances...\n`;
      buildLog += `üîß Commande d'installation: ${finalInstallCommand}\n`;
      await updateDeploymentLog(deploymentId, buildLog);

      // ‚úÖ Installation AVEC node_modules/.bin dans le PATH
      const installOutput = await execCommand(
        finalInstallCommand,
        { npm_config_platform: "linux" },
        300000,
        deploymentDir,
        deploymentDir
      );

      buildLog += `‚úÖ D√©pendances install√©es avec succ√®s\n`;

      // ‚úÖ V√âRIFICATION CRITIQUE: Tester Vite imm√©diatement
      try {
        // Test direct avec le PATH configur√©
        const viteVersion = await execCommand(
          "vite --version",
          {},
          30000,
          deploymentDir
        );
        buildLog += `‚úÖ Vite accessible: v${viteVersion.trim()}\n`;
      } catch (viteError) {
        buildLog += `‚ö†Ô∏è Vite non accessible directement, test avec npx...\n`;

        try {
          // Test avec npx (fallback)
          const viteVersionNpx = await execCommand(
            "npx vite --version",
            {},
            30000,
            deploymentDir
          );
          buildLog += `‚úÖ Vite accessible via npx: v${viteVersionNpx.trim()}\n`;
        } catch (npxError) {
          buildLog += `‚ùå Vite non trouv√©, installation forc√©e...\n`;

          // Installation explicite
          await execCommand(
            "npm install vite@^5.0.0 --save-dev --no-audit --no-fund",
            {},
            180000,
            deploymentDir
          );
          buildLog += `‚úÖ Vite install√© explicitement\n`;

          // V√©rification finale
          const viteFinal = await execCommand(
            "vite --version",
            {},
            30000,
            deploymentDir
          );
          buildLog += `‚úÖ Vite maintenant accessible: v${viteFinal.trim()}\n`;
        }
      }

      await updateDeploymentLog(deploymentId, buildLog);
    } catch (error) {
      buildLog += `‚ùå Erreur installation: ${error.message}\n`;
      await updateDeploymentLog(deploymentId, buildLog);
      throw error;
    }

    // ==================== BUILD DU PROJET ====================
    if (finalBuildCommand && finalBuildCommand !== "") {
      buildLog += `üèóÔ∏è [${new Date().toISOString()}] Build du projet...\n`;
      buildLog += `üîß Commande de build: ${finalBuildCommand}\n`;
      await updateDeploymentLog(deploymentId, buildLog);

      try {
        // ‚úÖ STRAT√âGIE DE BUILD ROBUSTE
        let buildSuccessful = false;
        let buildError = null;

        // Essai 1: Commande originale avec PATH configur√©
        try {
          const buildOutput = await execCommand(
            finalBuildCommand,
            { NODE_ENV: "production" },
            600000,
            deploymentDir,
            deploymentDir
          );
          buildLog += `‚úÖ Build r√©ussi (m√©thode standard)\n`;
          buildSuccessful = true;
        } catch (error1) {
          buildError = error1;
          buildLog += `‚ö†Ô∏è Build standard √©chou√©: ${error1.message}\n`;

          // Essai 2: Remplacer vite par npx vite
          if (finalBuildCommand.includes("vite build")) {
            buildLog += `üîÑ Tentative avec npx...\n`;
            await updateDeploymentLog(deploymentId, buildLog);

            try {
              const npxCommand = finalBuildCommand.replace(
                "vite build",
                "npx vite build"
              );
              const npxOutput = await execCommand(
                npxCommand,
                {
                  NODE_ENV: "production",
                  CI: "true",
                },
                600000,
                deploymentDir
              );
              buildLog += `‚úÖ Build r√©ussi avec npx\n`;
              buildSuccessful = true;
            } catch (error2) {
              buildLog += `‚ùå Build avec npx √©chou√©: ${error2.message}\n`;
            }
          }

          // Essai 3: Commande npm run build directe
          if (!buildSuccessful) {
            buildLog += `üîÑ Tentative avec npm run build direct...\n`;
            await updateDeploymentLog(deploymentId, buildLog);

            try {
              const npmOutput = await execCommand(
                "npm run build",
                {
                  NODE_ENV: "production",
                  CI: "true",
                },
                600000,
                deploymentDir
              );
              buildLog += `‚úÖ Build r√©ussi avec npm run build\n`;
              buildSuccessful = true;
            } catch (error3) {
              buildLog += `‚ùå Toutes les m√©thodes de build ont √©chou√©\n`;
            }
          }
        }

        if (!buildSuccessful) {
          throw (
            buildError || new Error("Build failed avec toutes les m√©thodes")
          );
        }
      } catch (buildError) {
        buildLog += `‚ùå Erreur build finale: ${buildError.message}\n`;
        throw buildError;
      }
    } else {
      buildLog += `‚ÑπÔ∏è Aucune commande de build sp√©cifi√©e, copie des fichiers statiques\n`;
    }

    // ==================== D√âPLOIEMENT DES FICHIERS ====================
    await supabase
      .from("deployments")
      .update({ status: "deploying", build_log: buildLog })
      .eq("id", deploymentId);

    buildLog += `üìÅ [${new Date().toISOString()}] D√©ploiement des fichiers...\n`;
    buildLog += `üîç Recherche du dossier de sortie: ${finalOutputDir}\n`;
    await updateDeploymentLog(deploymentId, buildLog);

    const sourceDir = path.join(deploymentDir, finalOutputDir);

    const possibleDirs = [
      sourceDir,
      path.join(deploymentDir, "out"),
      path.join(deploymentDir, ".next"),
      path.join(deploymentDir, "build"),
      path.join(deploymentDir, "dist"),
      path.join(deploymentDir, "public"),
      deploymentDir,
    ];

    let foundSourceDir = null;
    for (const dir of possibleDirs) {
      try {
        await fs.access(dir);
        const files = await fs.readdir(dir);
        if (
          files.includes("index.html") ||
          files.some((f) => f.endsWith(".html"))
        ) {
          foundSourceDir = dir;
          buildLog += `‚úÖ Fichiers trouv√©s dans: ${dir.replace(
            deploymentDir,
            "."
          )}\n`;
          break;
        }
      } catch (error) {
        continue;
      }
    }

    if (!foundSourceDir) {
      throw new Error(
        `Aucun dossier de sortie trouv√©. V√©rifi√©: ${possibleDirs
          .map((d) => d.replace(deploymentDir, "."))
          .join(", ")}`
      );
    }

    try {
      buildLog += `üìã Copie depuis ${foundSourceDir.replace(
        deploymentDir,
        "."
      )}\n`;

      await execCommand(
        `rm -rf "${outputDir}"/*`,
        {},
        30000,
        null,
        deploymentDir
      );
      await execCommand(
        `cp -r "${foundSourceDir}/"* "${outputDir}/" 2>/dev/null || true`,
        {},
        30000,
        null,
        deploymentDir
      );
      buildLog += `‚úÖ Fichiers copi√©s vers ${outputDir}\n`;

      const copiedFiles = await fs.readdir(outputDir);
      buildLog += `üìä Fichiers copi√©s: ${copiedFiles.length} √©l√©ments\n`;

      if (copiedFiles.includes("index.html")) {
        buildLog += `‚úÖ index.html trouv√©\n`;
      }
    } catch (copyError) {
      buildLog += `‚ùå Erreur copie: ${copyError.message}\n`;
      throw copyError;
    }

    // ==================== CONFIGURATION DU DOMAINE ====================
    await supabase
      .from("deployments")
      .update({ status: "configuring", build_log: buildLog })
      .eq("id", deploymentId);

    const domain = `${subdomain}.madahost.me`;

    await supabase
      .from("projects")
      .update({
        domain,
        status: "active",
        last_deployed: new Date().toISOString(),
      })
      .eq("id", project.id);

    buildLog += `‚úÖ [${new Date().toISOString()}] D√©ploiement r√©ussi!\n`;
    buildLog += `üåê Site disponible: https://${domain}\n`;
    if (primaryFramework) {
      buildLog += `üéØ Framework d√©ploy√©: ${primaryFramework.name}\n`;
    }

    await supabase
      .from("deployments")
      .update({
        status: "success",
        build_log: buildLog,
        completed_at: new Date().toISOString(),
      })
      .eq("id", deploymentId);

    setTimeout(async () => {
      try {
        await execCommand(
          `rm -rf ${deploymentDir}`,
          {},
          30000,
          null,
          deploymentDir
        );
        console.log(`üßπ Nettoyage termin√©: ${deploymentDir}`);
      } catch (error) {
        console.error("‚ùå Erreur nettoyage:", error);
      }
    }, 10000);
  } catch (error) {
    console.error(`‚ùå Erreur d√©ploiement ${deploymentId}:`, error);

    let finalLog = buildLog || "";
    finalLog += `‚ùå [${new Date().toISOString()}] Erreur: ${error.message}\n`;

    if (primaryFramework) {
      finalLog += `üîç Framework d√©tect√©: ${primaryFramework.name}\n`;
      finalLog += `üîç Config utilis√©e: ${JSON.stringify(
        primaryFramework.config,
        null,
        2
      )}\n`;
    }

    await supabase
      .from("deployments")
      .update({
        status: "failed",
        build_log: finalLog,
        completed_at: new Date().toISOString(),
      })
      .eq("id", deploymentId);

    try {
      await execCommand(
        `rm -rf ${deploymentDir}`,
        {},
        30000,
        null,
        deploymentDir
      );
    } catch (cleanupError) {
      console.error("‚ùå Erreur nettoyage:", cleanupError);
    }
  }
}

