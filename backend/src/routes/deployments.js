function execCommand(
  command,
  envVars = {},
  timeout = 300000,
  cwd = null,
  deploymentDir = null
) {
  return new Promise((resolve, reject) => {
    try {
      // ‚úÖ D√âTERMINATION DU R√âPERTOIRE
      const currentDir = cwd || deploymentDir || process.cwd();

      if (!currentDir) {
        return reject(new Error("Aucun r√©pertoire de travail sp√©cifi√©"));
      }

      // ‚úÖ CHEMIN ABSOLU pour node_modules/.bin
      const nodeBinPath = path.join(currentDir, "node_modules", ".bin");

      // ‚úÖ PATH CORRIG√â: Utiliser le chemin absolu du syst√®me
      const customPath = `${nodeBinPath}:/usr/local/bin:/usr/bin:/bin:/usr/local/sbin:/usr/sbin:/sbin`;

      // ‚úÖ V√âRIFIER SI VITE EXISTE DANS node_modules/.bin
      const viteBinaryPath = path.join(nodeBinPath, "vite");

      console.log(`üîß Ex√©cution: ${command}`);
      console.log(`üìÅ CWD: ${currentDir}`);
      console.log(`üîß PATH: ${customPath}`);
      console.log(`üîç Vite binary path: ${viteBinaryPath}`);

      const options = {
        timeout,
        cwd: currentDir,
        env: {
          ...process.env,
          ...envVars,
          PATH: customPath, // ‚úÖ PATH personnalis√©
          NODE_ENV: "production",
          // ‚úÖ Variables npm critiques
          npm_config_platform: "linux",
          npm_config_arch: "x64",
        },
        maxBuffer: 10 * 1024 * 1024,
      };

      // ‚úÖ V√âRIFICATION PR√âALABLE (debug)
      fs.access(viteBinaryPath)
        .then(() => console.log(`‚úÖ Vite binary existe: ${viteBinaryPath}`))
        .catch(() => console.log(`‚ùå Vite binary manquant: ${viteBinaryPath}`));

      exec(command, options, (error, stdout, stderr) => {
        if (error) {
          console.error(`‚ùå Erreur commande: ${command}`);
          console.error(`üìã Stdout: ${stdout}`);
          console.error(`üìã Stderr: ${stderr}`);

          // ‚úÖ DEBUG: Lister le contenu de node_modules/.bin
          const debugCommand = `ls -la ${nodeBinPath} | head -10`;
          exec(
            debugCommand,
            { cwd: currentDir },
            (debugError, debugStdout, debugStderr) => {
              console.log(`üîç Contenu de node_modules/.bin:`);
              console.log(debugStdout || debugStderr || "Aucun contenu");
            }
          );

          const errorMessage = `Command: ${command}\nError: ${error.message}\nStdout: ${stdout}\nStderr: ${stderr}`;
          reject(new Error(errorMessage));
          return;
        }

        if (stdout) console.log(`üìã Output: ${stdout}`);
        if (stderr && !stderr.includes("warning"))
          console.log(`‚ö†Ô∏è Warnings: ${stderr}`);

        resolve(stdout);
      });
    } catch (setupError) {
      reject(new Error(`Erreur configuration commande: ${setupError.message}`));
    }
  });
}

