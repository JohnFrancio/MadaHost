// ‚úÖ CORRECTION: Fonction execCommand avec deploymentDir en param√®tre
function execCommand(
  command,
  envVars = {},
  timeout = 300000,
  cwd = null,
  deploymentDir = null
) {
  return new Promise((resolve, reject) => {
    // ‚úÖ CORRECTION: Utiliser cwd OU deploymentDir, avec fallback s√©curis√©
    const currentDir = cwd || deploymentDir || process.cwd();

    if (!currentDir) {
      return reject(new Error("Aucun r√©pertoire de travail sp√©cifi√©"));
    }

    // ‚úÖ PATH CRITIQUE: Inclure node_modules/.bin du projet
    const nodeBinPath = path.join(currentDir, "node_modules", ".bin");
    const customPath = `${nodeBinPath}:${process.env.PATH}`;

    const options = {
      timeout,
      cwd: currentDir,
      env: {
        ...process.env,
        ...envVars,
        PATH: customPath,
        NODE_ENV: "production",
      },
      maxBuffer: 10 * 1024 * 1024,
    };

    console.log(`üîß Ex√©cution: ${command}`);
    console.log(`üìÅ CWD: ${options.cwd}`);
    console.log(`üîß PATH: ${customPath}`);

    exec(command, options, (error, stdout, stderr) => {
      if (error) {
        console.error(`‚ùå Erreur commande: ${command}`);
        console.error(`üìã Stdout: ${stdout}`);
        console.error(`üìã Stderr: ${stderr}`);

        const errorMessage = `Command: ${command}\nError: ${error.message}\nStdout: ${stdout}\nStderr: ${stderr}`;
        reject(new Error(errorMessage));
        return;
      }

      if (stdout) console.log(`üìã Output: ${stdout}`);
      if (stderr && !stderr.includes("warning"))
        console.log(`‚ö†Ô∏è Warnings: ${stderr}`);

      resolve(stdout);
    });
  });
}

